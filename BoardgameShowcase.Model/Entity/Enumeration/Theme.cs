namespace BoardgameShowcase.Model.Entity.Enumeration
{
    /// <summary>
    /// A boardgame theme.
    /// </summary>
    public enum Theme
    {
        /// <summary>
        /// Use it when the game takes place in the ancient era.
        /// </summary>
        Antiquity,
        /// <summary>
        /// Use it when the game takes place in the medieval era.
        /// </summary>
        Medieval,
        /// <summary>
        /// Use it when the game takes place in some future era.
        /// </summary>
        Science_Fiction,
        /// <summary>
        /// Use it when the game is about sciences.
        /// </summary>
        Sciences,
        /// <summary>
        /// Use it when the game is about economy.
        /// </summary>
        Economy,
        /// <summary>
        /// Use it when the game is about exploration.
        /// </summary>
        Exploration,
        /// <summary>
        /// Use it when the game takes place in Europe.
        /// </summary>
        Europe,
        /// <summary>
        /// Use it when the game takes place in the U.S.A.
        /// </summary>
        USA,
        /// <summary>
        /// Use it when the game is about trains.
        /// </summary>
        Trains,
        /// <summary>
        /// Use it when the game is about constructing something.
        /// </summary>
        Constructions,
        /// <summary>
        /// Use it when the game is about arts.
        /// </summary>
        Arts
    }
}
